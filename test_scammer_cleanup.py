"""
測試騙徒 Agent 回應清理
驗證是否正確移除策略分析等不必要內容
"""

import re

def clean_scammer_response(response: str) -> str:
    """清理騙徒回應中的策略分析"""
    
    # 移除策略分析標題和內容
    patterns_to_remove = [
        r'\*\*接下來的行動：\*\*.*?(?=\n\n|\Z)',  # 移除 **接下來的行動：** 及其內容
        r'\*\*核心目標：\*\*.*?(?=\n\n|\Z)',      # 移除 **核心目標：** 及其內容
        r'\*\*心理分析：\*\*.*?(?=\n\n|\Z)',      # 移除 **心理分析：** 及其內容
        r'\*\*策略：\*\*.*?(?=\n\n|\Z)',          # 移除 **策略：** 及其內容
        r'\* \*\*.*?：\*\*.*?(?=\n|\Z)',          # 移除 * **xxx：** 格式的列點
        r'^\*.*?(?=\n|\Z)',                       # 移除以 * 開頭的列點
    ]
    
    for pattern in patterns_to_remove:
        response = re.sub(pattern, '', response, flags=re.DOTALL | re.MULTILINE)
    
    # 移除多餘的空行
    response = re.sub(r'\n{3,}', '\n\n', response)
    response = response.strip()
    
    return response

