def get_scammer_examples(tactic: str, num_examples: int = 3) -> list:
    """
    獲取指定詐騙手法的示例
    
    Args:
        tactic: 詐騙手法類型
        num_examples: 返回的示例數量
        
    Returns:
        示例列表
    """
    # 映射詐騙手法到示例類別
    tactic_mapping = {
        "假冒銀行": "假冒銀行",
        "假網站冒充銀行": "假冒銀行",
        "假冒官員詐騙": "假冒政府",
        "假冒政府部門": "假冒政府",
        "虛假投資應用程式": "虛假投資",
        "投資詐騙": "虛假投資",
        "刷單騙案": "刷單詐騙",
        "網上兼職詐騙": "刷單詐騙",
        "愛情詐騙": "愛情詐騙",
        "網戀詐騙": "愛情詐騙",
        "WhatsApp 對話詐騙": "WhatsApp 假冒朋友",
        "假冒朋友": "WhatsApp 假冒朋友"
    }
    
    category = tactic_mapping.get(tactic, "假冒銀行")
    examples = SCAMMER_EXAMPLES.get(category, SCAMMER_EXAMPLES["假冒銀行"])
    return examples[:num_examples]


def format_scammer_examples_for_prompt(tactic: str, num_examples: int = 2) -> str:
    """
    將騙徒示例格式化為 Prompt 字符串
    
    Args:
        tactic: 詐騙手法
        num_examples: 示例數量
        
    Returns:
        格式化的示例字符串
    """
    examples = get_scammer_examples(tactic, num_examples)
    
    formatted = "\n\n## 📚 話術參考（內部學習，不要直接複製）\n\n"
    
    for i, example in enumerate(examples, 1):
        formatted += f"**情境 {i}：{example['scenario']}**\n"
        formatted += f"背景：{example['context']}\n"
        formatted += f"話術：「{example['output']}」\n\n"
    
    formatted += "**注意：以上只係參考，你要根據實際情況調整，保持自然對話。**\n\n"
    
    return formatted
