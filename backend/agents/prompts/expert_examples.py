def get_examples_for_persona(persona_type: str, num_examples: int = 3) -> list:
    """
    獲取指定 persona 類型的示例
    
    Args:
        persona_type: 受害者類型 (elderly/average/overconfident/student)
        num_examples: 返回的示例數量
        
    Returns:
        示例列表
    """
    examples = EXPERT_EXAMPLES.get(persona_type, EXPERT_EXAMPLES["average"])
    return examples[:num_examples]


def format_examples_for_prompt(persona_type: str, num_examples: int = 3) -> str:
    """
    將示例格式化為 Prompt 字符串
    
    Args:
        persona_type: 受害者類型
        num_examples: 示例數量
        
    Returns:
        格式化的示例字符串
    """
    examples = get_examples_for_persona(persona_type, num_examples)
    
    formatted = "\n\n## 📚 回應示例（請參考以下格式）\n\n"
    
    for i, example in enumerate(examples, 1):
        formatted += f"**示例 {i}：{example['scenario']}**\n\n"
        formatted += f"輸入：「{example['input']}」\n\n"
        formatted += f"輸出：\n{example['output']}\n\n"
        formatted += "---\n\n"
    
    return formatted
